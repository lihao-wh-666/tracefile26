-- ----------------------------------------
-- 行政区划表
-- ----------------------------------------
DROP TABLE IF EXISTS `region`;
CREATE TABLE `region` (
    `id` BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    `region_code` VARCHAR(20) NOT NULL UNIQUE COMMENT '行政区划代码',
    `region_name` VARCHAR(100) NOT NULL COMMENT '名称',
    `level` TINYINT NOT NULL COMMENT '级别: 1-省/直辖市/自治区/特别行政区, 2-地级市/地区/自治州, 3-区/县/县级市',
    `parent_id` BIGINT NOT NULL DEFAULT 0 COMMENT '父级ID',
    `sort` INT NOT NULL DEFAULT 0 COMMENT '排序',
    `status` TINYINT NOT NULL DEFAULT 1 COMMENT '状态: 1-启用, 0-禁用',
    `create_time` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `update_time` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX `idx_region_code` (`region_code`),
    INDEX `idx_parent_id` (`parent_id`),
    INDEX `idx_level` (`level`),
    INDEX `idx_status` (`status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='行政区划表';

