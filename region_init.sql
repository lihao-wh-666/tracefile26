DROP TABLE IF EXISTS district;
DROP TABLE IF EXISTS city;
DROP TABLE IF EXISTS province;

CREATE TABLE province (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    province_code VARCHAR(20) UNIQUE,
    province_name VARCHAR(100),
    sort INT DEFAULT 0,
    status TINYINT DEFAULT 1,
    create_time DATETIME,
    update_time DATETIME
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
ALTER TABLE province AUTO_INCREMENT = 1000;

CREATE TABLE city (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    city_code VARCHAR(20) UNIQUE,
    city_name VARCHAR(100),
    province_id BIGINT,
    sort INT DEFAULT 0,
    status TINYINT DEFAULT 1,
    create_time DATETIME,
    update_time DATETIME
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
ALTER TABLE city AUTO_INCREMENT = 10000;

CREATE TABLE district (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    district_code VARCHAR(20) UNIQUE,
    district_name VARCHAR(100),
    city_id BIGINT,
    sort INT DEFAULT 0,
    status TINYINT DEFAULT 1,
    create_time DATETIME,
    update_time DATETIME
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
ALTER TABLE district AUTO_INCREMENT = 100000;

